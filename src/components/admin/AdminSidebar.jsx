"use client";

import Link from "next/link";
import { usePathname } from "next/navigation";
import { useAuth } from "@/contexts/AuthContext";

const adminNav = [
  { name: "Dashboard", href: "/admin", icon: GridIcon },
  { name: "Properties", href: "/admin/properties", icon: BuildingIcon },
  { name: "Users", href: "/admin/users", icon: UsersIcon },
  { name: "Leads", href: "/admin/leads", icon: InboxIcon },
  { name: "Settings", href: "/admin/settings", icon: CogIcon },
];

export default function AdminSidebar() {
  const pathname = usePathname();
  const { user, logout } = useAuth();

  const isActive = (href) => {
    if (href === "/admin") return pathname === href;
    return pathname.startsWith(href);
  };

  const initials = user?.full_name
    ? user.full_name
        .split(" ")
        .map((n) => n[0])
        .join("")
        .toUpperCase()
        .slice(0, 2)
    : user?.phone?.slice(-2) || "A";

  return (
    <aside className="hidden lg:sticky lg:top-20 lg:flex lg:h-[calc(100vh-5rem)] lg:w-64 lg:flex-col lg:overflow-y-auto lg:border-r lg:border-border-light lg:bg-surface-white">
      <div className="flex flex-1 flex-col px-4 py-6">
        {/* User info */}
        <div className="mb-6 flex items-center gap-3 rounded-xl bg-surface-subtle px-4 py-3">
          <div className="flex h-10 w-10 items-center justify-center rounded-full bg-gradient-to-br from-primary-light to-primary text-sm font-semibold text-white">
            {initials}
          </div>
          <div className="min-w-0 flex-1">
            <p className="truncate text-sm font-semibold text-heading">
              {user?.full_name || "Admin"}
            </p>
            <span className="text-xs text-muted">Admin Panel</span>
          </div>
        </div>

        {/* Navigation */}
        <nav className="flex-1 space-y-1">
          {adminNav.map((item) => (
            <Link
              key={item.href}
              href={item.href}
              className={`flex items-center gap-3 rounded-xl px-4 py-2.5 text-sm font-medium transition-all ${
                isActive(item.href)
                  ? "bg-accent-soft text-accent-dark"
                  : "text-muted hover:bg-surface-subtle hover:text-heading"
              }`}
            >
              <item.icon className="h-5 w-5" />
              {item.name}
            </Link>
          ))}
        </nav>

        {/* Logout */}
        <button
          onClick={logout}
          className="mt-4 flex items-center gap-3 rounded-xl px-4 py-2.5 text-sm font-medium text-muted transition-all hover:bg-red-50 hover:text-red-500"
        >
          <LogoutIcon className="h-5 w-5" />
          Sign Out
        </button>
      </div>
    </aside>
  );
}

